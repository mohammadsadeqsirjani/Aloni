CREATE VIEW dbo.vw_storeExpertise
AS
SELECT        E.id, ISNULL(E.title, 'خالی') AS title, CASE WHEN isActive IS NULL OR
                         isActive = 0 THEN 'غیرفعال' WHEN isActive = 1 THEN 'فعال' END AS isActive, CASE WHEN isActive IS NULL OR
                         isActive = 0 THEN 'غیرفعال' WHEN isActive = 1 THEN 'فعال' END AS isActive_dsc, ISNULL(CAST(COUNT(SE.pk_fk_store_id) AS varchar(50)), 'خالی') AS store_cnt
FROM            dbo.TB_TYP_STORE_EXPERTISE AS E LEFT OUTER JOIN
                         dbo.TB_STORE_EXPERTISE AS SE WITH (nolock) ON E.id = SE.pk_fk_expertise_id
GROUP BY E.id, E.title, E.isActive
GO
