CREATE VIEW dbo.vw_technicalInfoTableGetList
AS
SELECT DISTINCT TITB.id, ISNULL(TITB.title, 'خالی') AS title, ISNULL(TITB.description, 'خالی') AS description, ISNULL(CAST(TITB.isActive AS varchar(50)), 'خالی') AS isActive, CASE WHEN TITB.isActive IS NULL OR
                         TITB.isActive = 1 THEN 'فعال' ELSE 'غیر فعال' END AS isActive_dsc, ISNULL(CAST(COUNT(TITB.id) AS varchar(50)), 'خالی') AS cnt
FROM            dbo.TB_TECHNICALINFO_TABLE AS TITB LEFT OUTER JOIN
                         dbo.TB_TYP_TECHNICALINFO AS TI ON TITB.id = TI.fk_technicalinfo_page_id
GROUP BY TITB.id, TITB.title, TITB.description, TITB.isActive

GO
